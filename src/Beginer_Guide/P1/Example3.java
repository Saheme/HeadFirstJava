package Beginer_Guide.P1;

public class Example3 {
    public static void main(String[] args) {
        int var1 = 10;
        double x = 10.0;
        System.out.println("Начальное значение var - " + var1);
        System.out.println("Начальное значение x - " + x);
        System.out.println();

        var1 = var1 / 4;
        x = x / 4;
        System.out.println("Значение var после деления - " + var1);
        System.out.println("Значение x после деления - " + x);
    }
}
